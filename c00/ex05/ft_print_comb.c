#include <unistd.h>

void	ft_print_comb(void)
{
	char	a;
	char	b;
	char	c;

	a = 0 + '0';
	while (a <= 7 + '0')
	{
		b = a + 1;
		while (b <= 8 + '0')
		{
			c = b + 1;
			while (c <= 9 + '0')
			{
				write(1, &a, 1);
				write(1, &b, 1);
				write(1, &c, 1);
				if (!((a == 7 + '0') && (b == 8 + '0') && (c == 9 + '0')))
					write(1, ", ", 2);
				c++;
			}
			b++;
		}
		a++;
	}
}
