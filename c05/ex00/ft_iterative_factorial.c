int	ft_iterative_factorial(int nb)
{
	int	factorial;

	factorial = 1;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	else
	{
		while (nb > 1)
		{
			factorial = factorial * nb;
			nb--;
		}
	}
	return (factorial);
}
